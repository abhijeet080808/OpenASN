#include "SequenceValue.hh"

#include "LoggingMacros.hh"
#include "ParseHelper.hh"
#include "ProductionFactory.hh"

#include "spdlog/spdlog.h"

using namespace OpenASN;

Production
SequenceValue::
GetType() const
{
  return Production::SEQUENCE_VALUE;
}

bool
SequenceValue::
Parse(const std::vector<Word>& asnData,
      size_t& asnDataIndex,
      std::vector<std::string>& endStop,
      std::vector<std::string>& parsePath)
{
  parsePath.push_back("SequenceValue");

  // SequenceValue ::= "{" ComponentValueList "}"

  size_t starting_index = asnDataIndex;

  auto obj = "{";
  LOG_START();
  if (ParseHelper::IsObjectPresent(obj, asnData, asnDataIndex))
  {
    ++asnDataIndex;
    LOG_PASS();
  }
  else
  {
    asnDataIndex = starting_index;
    LOG_FAIL();
    parsePath.pop_back();
    return false;
  }

  endStop.push_back("}");

  obj = "ComponentValueList";
  LOG_START();
  auto component_value_list =
    ProductionFactory::Get(Production::COMPONENT_VALUE_LIST);
  if (component_value_list->Parse(asnData, asnDataIndex, endStop, parsePath))
  {
    endStop.pop_back();
    mComponentValueList = component_value_list;
    LOG_PASS();
  }
  else
  {
    endStop.pop_back();
    asnDataIndex = starting_index;
    LOG_FAIL();
    parsePath.pop_back();
    return false;
  }

  obj = "}";
  LOG_START();
  if (ParseHelper::IsObjectPresent(obj, asnData, asnDataIndex))
  {
    ++asnDataIndex;
    LOG_PASS();
    parsePath.pop_back();
    return true;
  }
  else
  {
    asnDataIndex = starting_index;
    LOG_FAIL();
    parsePath.pop_back();
    return false;
  }
}
